package utils.maimai;

/**
 * 颜色数值
 * @author liuy
 *
 */
public class Color {
	private int r;
	private int g;
	private int b;
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getG() {
		return g;
	}
	public void setG(int g) {
		this.g = g;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
		
}
